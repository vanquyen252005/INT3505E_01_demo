package com.example.model;

public class BookV1 {
    private String title;
    private String author;

    public BookV1(String title, String author) {
        this.title = title;
        this.author = author;
    }

    // Getters & Setters
    public String getTitle() {
        return title;
    }

    public String getAuthor() {
        return author;
    }
}
